"""Testing a `ItIs` module."""

from __future__ import annotations

from xloft.itis import is_number


def test_is_number() -> None:
    """Testing a `is_number` method."""
    # Negative tests.
    for item in [
        "",
        " ",
        "123ayu456",
        "0x5",
        "0o5",
    ]:
        assert not is_number(item)
    # Positive tests.
    for item in [
        "-5.0",
        "+5.0",
        "5.0",
        ".5",
        "5.",
        "3.4E+38",
        "3.4E-38",
        "1.7E+308",
        "1.7E-308",
        "-1.7976931348623157e+308",
        "1.7976931348623157e+308",
        "255",
        "65535",
        "1677215",
        "4294967295",
        "281474976710655",
        "18446744073709551615",
        "79228162514264337593543950335",
        "340282366920938463463374607431768211455",
        "7809356576809509573609874689576897365487536545894358723468",
        "72028601076372765770200707816364342373431783018070841859646251155447849538676",
        "-255",
        "-65535",
        "-1677215",
        "-4294967295",
        "-281474976710655",
        "-18446744073709551615",
        "-79228162514264337593543950335",
        "-340282366920938463463374607431768211455",
        "-7809356576809509573609874689576897365487536545894358723468",
        "-72028601076372765770200707816364342373431783018070841859646251155447849538676",
    ]:
        assert is_number(item)
